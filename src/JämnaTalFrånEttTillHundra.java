public class JämnaTalFrånEttTillHundra {

    public static void main(String[] args) {

        int even = 2;

        System.out.println("Even numbers from 1 - 100 are:");

        while (even <= 100) {

            System.out.println(even);

            even = even + 2; // kan skrivas som even += 2;
        }
    }
}
